var isOn = false;

Dino.addEventListener("mouseover",() => {
    if(!isOn)
    {
    document.getElementById("dino-holder-1").style.display = "block";
    }
});

Dino.addEventListener("mouseout",() => {
    if(!isOn)
    {
        document.getElementById("dino-holder-1").style.display = "none";
    }
});

Dino.addEventListener("click",() => {
    if(isOn)
    {
        document.getElementById("dino-holder-1").style.display = "none";
        isOn = false;
    }
    else
    {
        document.getElementById("dino-holder-1").style.display = "block";
        isOn = true;
    }
});



